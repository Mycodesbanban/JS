// operador de Atribuição , atribui um valor ao operando à sua esquerda baseado no valor do operando à direita. O operador de atribuição básico é o igual (=), que atribui o valor do operando à direita ao operando à esquerda.
const a=7;
let b = 3;
b+=a; // ou b= b+a
console.log(b);
b-=4; // ou b= b-4
console.log(b);
b*=2;// ou b= b * 2
console.log(b)
b/=2; //ou b= b / 2
console.log(b)
// agora vamos fazer uma modula
b%=2 // ou b= b % 2
// se o valor for par ele vai imprimir 0 ser for impar vai imprimir 1
console.log(b)
// o resultado foi 0 então par 